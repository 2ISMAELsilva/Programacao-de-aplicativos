// Operadores

let idade = 10
let peso = '10'

console.log('- Comparação')
console.log('Comparando conteudo')
console.log('Idade e peso são difentes?')
console.log(idade == peso)

console.log('\nComparando tipo e conteudo')
console.log('Idade e peso são difentes?')
console.log(idade === peso)

console.log('- DIFERENÇA -')
console.log('\nComparando conteudo')
console.log('Idade e peso são difentes?')
console.log(idade != peso)

console.log('\nComparando conteudo')
console.log('Idade e peso são difentes?')
console.log(idade !== peso)
